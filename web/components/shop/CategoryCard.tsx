interface CategoryCardProps {
  category: CategoryWithChildren;
  showVendorCount?: boolean;
}

export function CategoryCard({
  category,
  showVendorCount = true,
}: CategoryCardProps) {
  const icon = CategoryIcons[category.slug] || (
    <svg className="h-8 w-8" fill="none" viewBox="0 0 24 24" stroke="currentColor">
      <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={1.5} d="M2.25 12.75V12A2.25 2.25 0 014.5 9.75h15A2.25 2.25 0 0121.75 12v.75m-8.69-6.44l-2.12-2.12a1.5 1.5 0 00-1.061-.44H4.5A2.25 2.25 0 002.25 6v12a2.25 2.25 0 002.25 2.25h15A2.25 2.25 0 0021.75 18V9a2.25 2.25 0 00-2.25-2.25h-5.379a1.5 1.5 0 01-1.06-.44z" />
    </svg>
  );

  return (
    <Link
      href={`/shop/category/${category.slug}`}
      className="group flex flex-col items-center rounded-2xl border border-slate-800 bg-[#08090C] p-6 text-center transition hover:-translate-y-1 hover:border-[#14B8A6]/70 hover:shadow-lg hover:shadow-[#14B8A6]/10"
    >
      <div className="rounded-xl bg-slate-800/50 p-4 text-[#14B8A6] transition group-hover:bg-[#14B8A6]/10 group-hover:text-[#14B8A6]">
        {icon}
      </div>
      <h3 className="mt-4 font-semibold text-slate-100 group-hover:text-[#14B8A6]">
        {category.name}
      </h3>
      {showVendorCount && (
        <p className="mt-1 text-sm text-slate-500">
          {category.vendorCount} {category.vendorCount === 1 ? "vendor" : "vendors"}
        </p>
      )}
    </Link>
  );
}

/**
 * Grid of category cards
 */
interface CategoryGridProps {
  categories: CategoryWithChildren[];
  showVendorCount?: boolean;
}

export function CategoryGrid({
  categories,
  showVendorCount = true,
}: CategoryGridProps) {
  return (
    <div className="grid grid-cols-2 gap-4 sm:grid-cols-3 md:grid-cols-4 lg:grid-cols-7">
      {categories.map((category) => (
        <CategoryCard
          key={category.id}
          category={category}
          showVendorCount={showVendorCount}
        />
      ))}
    </div>
  );
}

