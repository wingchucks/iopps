/**
 * EmptyState - Displays a centered empty state message with optional CTA
 *
 * Supports two CTA APIs for backwards compatibility:
 * 1. New API: action={{ label: "...", href: "..." }}
 * 2. Legacy API: ctaLabel="..." ctaHref="..." or onCta={...}
 */
export function EmptyState({
  icon = "search",
  title,
  description,
  action,
  ctaLabel,
  ctaHref,
  onCta,
  testId,
  className = "",
}: EmptyStateProps) {
  // Normalize the two APIs - prefer new API if both provided
  const label = action?.label || ctaLabel;
  const href = action?.href || ctaHref;
  const hasCta = label && (href || onCta);

  return (
    <div
      className={`mx-auto flex max-w-xl flex-col items-center justify-center rounded-2xl border border-[var(--card-border)] bg-surface px-6 py-12 text-center ${className}`}
      data-testid={testId}
    >
      <div className="mb-4 rounded-full bg-surface p-4 text-[var(--text-muted)]">
        {icons[icon]}
      </div>
      <h3 className="text-lg font-semibold text-foreground">{title}</h3>
      <p className="mt-2 max-w-md text-sm text-[var(--text-muted)]">{description}</p>
      {hasCta && (
        <>
          {href ? (
            <Link
              href={href}
              className="mt-6 inline-flex items-center gap-2 rounded-full bg-accent px-6 py-3 font-semibold text-white transition-all hover:bg-accent hover:shadow-lg hover:shadow-teal-500/20"
            >
              {label}
            </Link>
          ) : (
            <button
              onClick={onCta}
              className="mt-6 inline-flex items-center gap-2 rounded-full bg-accent px-6 py-3 font-semibold text-white transition-all hover:bg-accent hover:shadow-lg hover:shadow-teal-500/20"
            >
              {label}
            </button>
          )}
        </>
      )}
    </div>
  );
}
